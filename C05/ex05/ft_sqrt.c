int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb <= 0 || nb > 46340 * 46340)
		return (0);
	while (i <= nb / i)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
