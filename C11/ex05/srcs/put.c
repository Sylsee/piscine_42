#include "../includes/ft.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		str_len(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

void	ft_putnbr(int nb)
{
	long tmp;

	tmp = nb;
	if (tmp < 0)
	{
		ft_putchar('-');
		tmp = tmp * (-1);
	}
	if (tmp > 9)
	{
		ft_putnbr(tmp / 10);
		ft_putnbr(tmp % 10);
	}
	else
		ft_putchar(tmp + '0');
}
