int	ft_count_if(char **tab, int length, int (*f)(char *))
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (j < length)
	{
		if (f(tab[j]) != 0)
			i++;
		j++;
	}
	return (i);
}
